    private void tblBoTriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBoTriMouseClicked
        try {
            int row = tblBoTri.getSelectedRow();
            if (row >= 0) {
                String MaNV = (String) tblBoTri.getValueAt(row, 0);
                String Ngay = (String) tblBoTri.getValueAt(row, 1);
                BienDongDAO dao = new BienDongDAO();
                BienDongNhanSu nv = dao.FindNhanVien(MaNV, Ngay);
                if (nv != null) {
                    cbbMaNV.setSelectedItem(nv.getMaNV());
                    dpNgay.setDate(fm.parse(Ngay));
                    cbbCV.setSelectedItem(nv.getTenCV_M());
                    cbbBP.setSelectedItem(nv.getTenBP_M());
                    txtHSL_M.setText(Format.FormatNumber(nv.getHeSoLuong_M()));
                    txtCV_C.setText(nv.getTenCV_C());
                    txtBP_C.setText(nv.getTenBP_C());
                    txtHSL_C.setText(Format.FormatNumber(nv.getHeSoLuong_C()));
                }
            }
        } catch (Exception ex) {
            MessageDialogHelper.showError(home, ex.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblBoTriMouseClicked

