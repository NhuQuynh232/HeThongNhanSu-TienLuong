    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        StringBuilder sb = new StringBuilder();
        if (sb.length() > 0) {
            MessageDialogHelper.showError(home, sb.toString(), "Lỗi");
            return;
        }
        if (MessageDialogHelper.showConfirm(home, "Tiếp tục xóa?", "Thông báo") == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            BienDongDAO dao = new BienDongDAO();
            if (dao.delete(cbbMaNV.getSelectedItem().toString(), fm.format(dpNgay.getDate()))) {
                MessageDialogHelper.showMessage(home, "Đã xóa", "Thông báo");
                LoadTable();
            } else {
                MessageDialogHelper.showError(home, "Không thể lưu", "Lỗi");
            }
        } catch (Exception ex) {
            MessageDialogHelper.showError(home, ex.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private BienDongNhanSu SetBienDong() {
        BienDongNhanSu bd = new BienDongNhanSu();
        bd.setMaNV(cbbMaNV.getSelectedItem().toString());
        bd.setNgayHieuLuc(fm.format(dpNgay.getDate()));
        bd.setMaBP_C(lblBP_C.getText());
        bd.setMaCV_C(lblCV_C.getText());
        bd.setHeSoLuong_C(Format.RemoveFormat(txtHSL_C.getText()));
        bd.setMaBP_M(lblMaBP.getText());
        bd.setMaCV_M(lblMaCV.getText());
        bd.setHeSoLuong_M(Format.RemoveFormat(txtHSL_M.getText()));
        return bd;
    }


