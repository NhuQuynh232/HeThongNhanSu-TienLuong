    private void btnThmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThmActionPerformed
        StringBuilder sb=new StringBuilder();
        if(sb.length()>0){
            MessageDialogHelper.showError(home, sb.toString(), "Lỗi");
            return;
        }
        try{
            BoPhan nv=setBoPhan();
            BoPhanDAO dao=new BoPhanDAO();
            BoPhan bp = dao.FindBPByMa(txtMaBP.getText());
            if (MessageDialogHelper.showConfirm(home, "Tiếp tục thưc hiện?", "Thông báo") == JOptionPane.NO_OPTION) {
                return;
            }
            if (bp != null) {
                if (dao.update(nv)) {
                    MessageDialogHelper.showMessage(home, "Đã lưu", "Thông báo");
                    LoadTable();
                    New();
                } else {
                    MessageDialogHelper.showError(home, "Không thể lưu", "Lỗi");
                }
            } else {
                if (dao.insert(nv)) {
                    MessageDialogHelper.showMessage(home, "Đã lưu", "Thông báo");
                    LoadTable();
                    New();
                } else {
                    MessageDialogHelper.showError(home, "Không thể lưu", "Lỗi");
                }
            }
        } catch (Exception ex) {
            MessageDialogHelper.showError(home, ex.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnThmActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        StringBuilder sb=new StringBuilder();
        if(sb.length()>0){
            MessageDialogHelper.showError(home, sb.toString(), "Lỗi");
            return;
        }
        if(MessageDialogHelper.showConfirm(home, "Tiếp tục xóa?", "Thông báo")==JOptionPane.NO_OPTION){
            return;
        }
        try {
            BoPhanDAO dao = new BoPhanDAO();
            dao.delete(txtMaBP.getText().trim());
            MessageDialogHelper.showMessage(home, "Đã xóa", "Thông báo");
            LoadTable();
            New();
        } catch (Exception ex) {
            MessageDialogHelper.showError(home, ex.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblBoPhanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBoPhanMouseClicked
        try{
            int row= tblBoPhan.getSelectedRow();
            if(row>=0){
                String MaBP=(String)tblBoPhan.getValueAt(row, 0);
                BoPhanDAO dao= new BoPhanDAO();
                BoPhan nv=dao.FindBPByMa(MaBP);
                if( nv!=null){
                    txtMaBP.setText(nv.getMaPB());
                    txtTenBP.setText(nv.getTenPB());
                    txtemail.setText(nv.getEmail());
                    txtGhiChu.setText(nv.getGhiChu());
                    txtSDT.setText(nv.getSDT());
                }
            }
        }catch (Exception ex){
            MessageDialogHelper.showError(home, ex.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblBoPhanMouseClicked

    private void processLoginSuccessful(){
        if(ShareData.login.getDep().equals("All")){
            btnThm.setEnabled(true);
            btnXoa.setEnabled(true);
        } else {
            btnThm.setEnabled(false);
            btnXoa.setEnabled(false);
            btnThm.setToolTipText("Tài khoản không được cấp quyền ");
            btnXoa.setToolTipText("Tài khoản không được cấp quyền ");
        }
    }
    
    private void New(){
        txtMaBP.setText("");
        txtGhiChu.setText("");
        txtSDT.setText("");
        txtTenBP.setText("");
        txtemail.setText("");
    }
    
    private BoPhan setBoPhan(){
        BoPhan bp=new BoPhan();
        bp.setMaBP(txtMaBP.getText());
        bp.setTenPB(txtTenBP.getText());
        bp.setGhiChu(txtGhiChu.getText());
        bp.setSDT(txtSDT.getText());
        bp.setEmail(txtemail.getText());
        return bp;
    }

