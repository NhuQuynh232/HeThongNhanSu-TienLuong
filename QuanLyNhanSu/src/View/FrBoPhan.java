    private void LoadTable(){
        try{
            BoPhanDAO dao=new BoPhanDAO();
            List<BoPhan> list;
            list = dao.FindAllBoPhan();
            tblmodel.setRowCount(0);
            list.forEach(it -> {
                tblmodel.addRow(new Object[]{it.getMaPB(),it.getTenPB(),it.getEmail(),it.getSDT(),it.getGhiChu()});
            });
            tblmodel.fireTableDataChanged();
        }catch(Exception ex){
            MessageDialogHelper.showError(home, ex.toString(), "Lỗi");
        }
    }

