    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        StringBuilder sb=new StringBuilder();
        if(sb.length()>0){
            MessageDialogHelper.showError(home, sb.toString(), "Lỗi");
            return;
        }
        if(MessageDialogHelper.showConfirm(home, "Tiếp tục xóa?", "Thông báo")==JOptionPane.NO_OPTION){
            return;
        }
        try{
            KhenThuongDAO dao=new KhenThuongDAO();
            if(dao.delete(cbbMaNV.getSelectedItem().toString(),fm.format(dpNgay.getDate()))){
                MessageDialogHelper.showMessage(home, "Đã lưu", "Thông báo");
                LoadTable();
                New();
            }else {
                MessageDialogHelper.showError(home, "Không thể lưu", "Lỗi");
            }
        }catch(Exception ex){
            MessageDialogHelper.showError(home, ex.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThmActionPerformed
        StringBuilder sb=new StringBuilder();
        if(sb.length()>0){
            MessageDialogHelper.showError(home, sb.toString(), "Lỗi");
            return;
        }
        try{
            KhenThuong nv = setKhenThuong();
            KhenThuongDAO dao = new KhenThuongDAO();
            KhenThuong kt = dao.FindNhanVien(cbbMaNV.getSelectedItem().toString(), fm.format(dpNgay.getDate()));
            if (kt != null) {
                if (dao.update(nv)) {
                    MessageDialogHelper.showMessage(home, "Đã lưu", "Thông báo");
                    LoadTable();
                    New();
                } else {
                    MessageDialogHelper.showError(home, "Không thể lưu", "Lỗi");
                }
            } else {
                if (dao.insert(nv)) {
                    MessageDialogHelper.showMessage(home, "Đã lưu", "Thông báo");
                    LoadTable();
                    New();
                } else {
                    MessageDialogHelper.showError(home, "Không thể lưu", "Lỗi");
                }
            }
        }catch(Exception ex){
            MessageDialogHelper.showError(home, ex.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnThmActionPerformed

    private void tblKhenThuongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhenThuongMouseClicked
        try{
            int row= tblKhenThuong.getSelectedRow();
            if(row>=0){
                String MaNV=(String)tblKhenThuong.getValueAt(row, 0);
                String Ngay=(String)tblKhenThuong.getValueAt(row, 1);
                KhenThuongDAO dao= new KhenThuongDAO();
                KhenThuong nv=dao.FindNhanVien(MaNV,Ngay);
                if( nv!=null){
                    cbbMaNV.setSelectedItem(nv.getMaNV());
                    dpNgay.setDate(fm.parse(nv.getNgayKhenThuong()));
                    txtMucKhenThuong.setText(Format.FormatNumber(nv.getMucKhenThuong()));
                    txtGhiChu.setText(nv.getGhiChu());
                }
            }
        }catch (Exception ex){
            MessageDialogHelper.showError(home, ex.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblKhenThuongMouseClicked

    private void New(){
        txtMucKhenThuong.setText("");
        txtGhiChu.setText("");
    }
    
    private KhenThuong setKhenThuong(){
        KhenThuong kt=new KhenThuong();
        kt.setMaNV(cbbMaNV.getSelectedItem().toString());
        kt.setMucKhenThuong(Format.RemoveFormat(txtMucKhenThuong.getText()));
        kt.setNgayKhenThuong(fm.format(dpNgay.getDate()));
        kt.setGhiChu(txtGhiChu.getText());
        return kt;
    }

