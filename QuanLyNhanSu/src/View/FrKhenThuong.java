    private void LoadTable(){
        try{
            KhenThuongDAO dao=new KhenThuongDAO();
            List<KhenThuong> list;
            list = dao.FindAllBoTri();
            tblmodel.setRowCount(0);
            list.forEach(it -> {
                tblmodel.addRow(new Object[]{it.getMaNV(),it.getNgayKhenThuong(),Format.FormatNumber(it.getMucKhenThuong()),it.getGhiChu()});
            });
            tblmodel.fireTableDataChanged();
        }catch(Exception ex){
            MessageDialogHelper.showError(home, ex.toString(), "Lỗi");
        }
    }
    private void DesignTable(){
        DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
        rightRenderer.setHorizontalAlignment(JLabel.RIGHT);
        tblKhenThuong.setModel(tblmodel);
        tblKhenThuong.getColumnModel().getColumn(2).setCellRenderer(rightRenderer);
    }
    
