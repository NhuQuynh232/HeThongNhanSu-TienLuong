    private void tblKyLuatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKyLuatMouseClicked
        try{
            int row= tblKyLuat.getSelectedRow();
            if(row>=0){
                String MaNV=(String)tblKyLuat.getValueAt(row, 0);
                String Ngay=(String)tblKyLuat.getValueAt(row, 1);
                KyLuatDAO dao= new KyLuatDAO();
                KyLuat nv=dao.FindNhanVien(MaNV,Ngay);
                if( nv!=null){
                    cbbMaNV.setSelectedItem(nv.getMaNV());
                    dpNgay.setDate(fm.parse(nv.getNgayKyLuat()));
                    txtMucKyLuat.setText(Format.FormatNumber(nv.getMucKyLuat()));
                    txtGhiChu.setText(nv.getGhiChu());
                }
            }
        }catch (Exception ex){
            MessageDialogHelper.showError(home, ex.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblKyLuatMouseClicked

    
    private void New(){
        txtMucKyLuat.setText("");
        txtGhiChu.setText("");
    }
    
    private KyLuat setKyLuat(){
        KyLuat kt=new KyLuat();
        kt.setMaNV(cbbMaNV.getSelectedItem().toString());
        kt.setMucKyLuat(Format.RemoveFormat(txtMucKyLuat.getText()));
        kt.setNgayKyLuat(fm.format(dpNgay.getDate()));
        kt.setGhiChu(txtGhiChu.getText());
        return kt;
    }

