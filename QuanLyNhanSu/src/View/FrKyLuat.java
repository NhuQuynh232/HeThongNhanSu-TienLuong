    private void initTable(){
        tblmodel =new DefaultTableModel();
        tblmodel.setColumnIdentifiers(new String[]{"Mã nhân viên","Ngày kỷ luật","Mức kỷ luật","Ghi chú"});
        tblKyLuat.setModel(tblmodel);
        TableDesign.Design(tblKyLuat);
    }
    
    private void LoadTable(){
        try{
            KyLuatDAO dao=new KyLuatDAO();
            List<KyLuat> list;
            list = dao.FindAllBoTri();
            tblmodel.setRowCount(0);
            list.forEach(it -> {
                tblmodel.addRow(new Object[]{it.getMaNV(),it.getNgayKyLuat(),Format.FormatNumber(it.getMucKyLuat()),it.getGhiChu()});
            });
            tblmodel.fireTableDataChanged();
        }catch(Exception ex){
            MessageDialogHelper.showError(home, ex.toString(), "Lỗi");
        }
    }
    private void DesignTable(){
        DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
        rightRenderer.setHorizontalAlignment(JLabel.RIGHT);
        tblKyLuat.setModel(tblmodel);
        tblKyLuat.getColumnModel().getColumn(2).setCellRenderer(rightRenderer);
    }
    private void LoadComboBox(){
        try{
            cbbMaNV.removeAllItems();
            NhanVienDAO dao3=new NhanVienDAO();
            List<Model.NhanVien> list3= dao3.FindAllNhanVien();
            list3.forEach(it -> {
                cbbMaNV.addItem(it.getMaNV());
            });
        }catch(Exception ex){
            MessageDialogHelper.showError(home, ex.toString(), "Lỗi");
        }
    }
    
