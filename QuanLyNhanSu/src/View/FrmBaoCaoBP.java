    private void LoadTable(){
        try{
            NhanVienDAO dao=new NhanVienDAO();
            List<NhanVien> list;
            list = dao.FindNhanVien_v();
            tblmodel.setRowCount(0);
            list.forEach(it -> {
                tblmodel.addRow(new Object[]{it.getMaNV(),it.getTenNV(),it.getTenCV(),it.getTenPB()});
            });
            tblmodel.fireTableDataChanged();
        }catch(Exception ex){
            MessageDialogHelper.showError(home, ex.toString(), "Lỗi");
        }
    }
    private void LoadComboBox(){
        try{
            cbbBP.removeAllItems();
            cbbBP.addItem("Tất cả");
            BoPhanDAO dao1=new BoPhanDAO();
            List<BoPhan> list1= dao1.FindAllBoPhan();
            list1.forEach(it -> {
                cbbBP.addItem(it.getTenPB());
            });
        }catch(Exception ex){
            MessageDialogHelper.showError(home, ex.toString(), "Lỗi");
        }
        
    }

