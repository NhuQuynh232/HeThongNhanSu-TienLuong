    private void tblBaoCaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBaoCaoMouseClicked
        try{
            int row= tblBaoCao.getSelectedRow();
            if(row>=0){
                String MaNV=(String)tblBaoCao.getValueAt(row, 0);
                NhanVienDAO dao= new NhanVienDAO();
                NhanVien nv=dao.FindNhanVien(MaNV);
                if( nv!=null){
                    txtMaNV.setText(nv.getMaNV());
                    txtTenNV.setText(nv.getTenNV());
                    cbbBP.setSelectedItem(nv.getTenPB());
                    txtCV.setText(nv.getTenCV());
                }
            }
        }catch (Exception ex){
            MessageDialogHelper.showError(home, ex.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblBaoCaoMouseClicked

